
// Demonstrating valid and invalid identifiers in Java
public class ValidIdentifiers {
    public static void main(String[] args) {
        int valid1 = 10;
        int _valid2 = 20;
        int $valid3 = 30;
        int validVariable = 40;

        System.out.println(valid1 + ", " + _valid2 + ", " + $valid3 + ", " + validVariable);
    }
}
